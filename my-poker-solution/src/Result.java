
class Result {

    private int priority = -1;
    private int card = -1;

    public int getPriority() {
        return priority;
    }

    public int setPriority(int priority) {
        this.priority = priority;
        return this.priority;
    }

    public int getCard() {
        return card;
    }

    public int setCard(int card) {
        this.card = card;
        return this.card;
    }

}
